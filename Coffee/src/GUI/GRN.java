    private void QuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_QuantityActionPerformed
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

        Double a = Double.parseDouble(Quantity.getText());
        Double b = Double.parseDouble(Unit_Price.getText());
        Double c = a * b;

        Vector v = new Vector();

        v.add(txt_productID.getText());
        v.add(Product_Name.getText());
        v.add(Quantity.getText());
        v.add(Unit_Price.getText());
        v.add(c);
        dtm.addRow(v);

        Double sub = 0.0;

        for (int i = 0; i < jTable1.getRowCount(); i++) {
            sub += Double.parseDouble(dtm.getValueAt(i, 4).toString());
            Sub_Total.setText("" + sub + "0");
            Unit_Price.setText("");
            Quantity.setText("");
            jTextField2.grabFocus();

        }


    }//GEN-LAST:event_QuantityActionPerformed

