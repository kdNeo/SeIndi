    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int x = 0;
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        try {

            for (int i = 0; i < jTable1.getRowCount(); i++) {
                String pid = jTable1.getValueAt(i, 0).toString();
                String p_name = jTable1.getValueAt(i, 1).toString();
                String quantity = jTable1.getValueAt(i, 2).toString();
                String unit_price = jTable1.getValueAt(i, 3).toString();
                System.out.println(unit_price);
                String sub_total = jTable1.getValueAt(i, 4).toString();

                DB.iud("insert into grn (supplier_id, supplier_name, product_name, qty, unit_price,  date, time) values "
                        + "('" + Supplire_ID.getText() + "', '" + Supplier_name.getText() + "', '" + p_name + "', '" + quantity + "', '" + unit_price + "',  '" + Date.getText() + "', '" + Time.getText() + "')");

                ResultSet rs = DB.search("select gid from grn where gid='" + jTextField1.getText() + "'");
                if (rs.next()) {
                    x = Integer.parseInt(rs.getString("gid").toString());
                }

                DB.iud("insert into grn_item(stock_sid, grn_gid, product_name, unit_price, qty, total, sub_total, date, time) values "
                        + "('" + pid + "', '" + x + "', '" + p_name + "', '" + unit_price + "', '" + quantity + "', '" + sub_total + "','" + Sub_Total.getText() + "','" + Date.getText() + "', '" + Time.getText() + "')");
                //

                ResultSet rsz = DB.search("SELECT  * FROM stock s  WHERE s.name = '" + p_name + "' AND s.qty = '' AND s.unit_price = ''");
                if (rsz.first()) {
                    DB.iud("update stock set qty=qty+'" + quantity + "',unit_price='" + unit_price + "', buying_price='" + Sub_Total.getText() + "' where sid='" + rsz.getString("sid") + "'");
                } else {
                    DB.iud("INSERT INTO stock (name,qty,unit_price,buying_price) VALUES ('" + p_name + "','" + quantity + "','" + unit_price + "','" + sub_total + "')");
                }

            }
            JOptionPane.showMessageDialog(rootPane, "GRN HAS SAVED!");

        } catch (Exception e) {
            e.printStackTrace();
        }

        dtm.setRowCount(0);
        clerText();
        autogen();
        this.dispose();
        MenuForm m = new MenuForm();
        m.setVisible(true);
    }//GEN-LAST:event_jButton1ActionPerformed

