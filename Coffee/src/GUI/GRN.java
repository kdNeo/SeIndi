    private void Searchfield2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Searchfield2KeyReleased
        try {
            ResultSet rs = DB.search("select fname from supplier where fname like '" + Searchfield2.getText() + "%'");
            //
            Vector v = new Vector();

            while (rs.next()) {
                jScrollPane2.setVisible(true);
                v.add(rs.getString("fname"));
            }

            jList2.setListData(v);

            if (Searchfield2.getText().isEmpty()) {
                jScrollPane2.setVisible(false);
            }
            if (evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_UP) {
                jList2.grabFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_Searchfield2KeyReleased

