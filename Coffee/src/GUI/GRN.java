    private void Searchfiled1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_Searchfiled1KeyReleased

        try {
            ResultSet rs = DB.search("select name from stock where name like '" + Searchfiled1.getText() + "%'");

            Vector v = new Vector();
            while (rs.next()) {

                jScrollPane1.setVisible(true);
                v.add(rs.getString("name"));

            }
            jList1.setListData(v);
            if (Searchfiled1.getText().isEmpty()) {
                jScrollPane1.setVisible(false);
            }
            if (evt.getKeyCode() == KeyEvent.VK_DOWN || evt.getKeyCode() == KeyEvent.VK_UP) {
                jList1.grabFocus();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_Searchfiled1KeyReleased

    private void jList1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jList1KeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                ResultSet rs = DB.search("select name from stock where name='" + jList1.getSelectedValue().toString() + "'");
                while (rs.next()) {
                    Searchfiled1.setText(rs.getString("name"));
                    String s = Searchfiled1.getText();
                    Product_Name.setText(s);

                }

                ResultSet rs1 = DB.search("select sid from stock where name='" + jList1.getSelectedValue().toString() + "'");
                while (rs1.next()) {
                    txt_productID.setText(rs1.getString("sid"));
                    jScrollPane1.setVisible(false);
                    Searchfiled1.setText("");
                    Unit_Price.requestFocus();

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jList1KeyReleased

