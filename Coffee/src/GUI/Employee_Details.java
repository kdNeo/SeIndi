    private void searchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMouseClicked
        try {

            
            ResultSet rs = DB.search("select * from user where id='" + src.getText() + "'");
            if (rs.next()) {
                id.setText(rs.getString("id"));
                fn.setText(rs.getString("fname"));
                ln.setText(rs.getString("lname"));
                ad.setText(rs.getString("address"));
                ci.setText(rs.getString("city"));
                cn.setText(rs.getString("contact"));

                String gen = rs.getString("gender");
                if (gen.equals("male")) {
                    male.setSelected(true);
                } else {
                    female.setSelected(true);
                }

                date.setDate(rs.getDate("date_of_join"));
                sa.setText(rs.getString("salary"));
                em.setText(rs.getString("email"));
                
               if(Integer.parseInt(id.getText())==Integer.parseInt(src.getText())){
               save.setEnabled(false);
               }
            }

        } catch (Exception e) {
          e.printStackTrace();
        }
    }//GEN-LAST:event_searchMouseClicked

