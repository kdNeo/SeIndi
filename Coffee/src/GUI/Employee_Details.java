    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
        String idd = id.getText();
        String fname = fn.getText();
        String lname = ln.getText();
        String addr = ad.getText();
        String city = ci.getText();
        String cont = cn.getText();
        String gen = "";
        if (male.isSelected()) {
            gen = "male";
        } else {
            gen = "female";
        }

        SimpleDateFormat dcn = new SimpleDateFormat("yyyy-MM-dd");
        String date1 = dcn.format(date.getDate());

        String sala = sa.getText();
        String ema = em.getText();

        try {

          if(  DB.iud("insert  into user values('" + idd + "','" + fname + "','" + lname + "','" + addr + "','" + city + "','" + cont + "','" + gen + "','" + date1 + "','" + sala + "','" + ema + "')")){
           // JOptionPane.showMessageDialog(rootPane, " Saved!");
            fn.setText(null);
            ln.setText(null);
            ad.setText(null);
            ci.setText(null);
            cn.setText(null);
            male.setSelected(true);

            date.setDate(null);
            sa.setText(null);
            em.setText(null);
            generateId();
            
          }else{
                JOptionPane.showMessageDialog(this, "cannot insert duplicate employee id"," ", JOptionPane.WARNING_MESSAGE);
          }
            

        } catch (Exception e) {
          e.printStackTrace();
        }
    }//GEN-LAST:event_saveMouseClicked

